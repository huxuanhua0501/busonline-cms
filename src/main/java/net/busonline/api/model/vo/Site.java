package net.busonline.api.model.vo;

/**
 * 站点信息
 * @author win7
 *
 */
public class Site {
	private String name;
	private String lat;
	private String lon;
	private String glat;
	private String glon;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLat() {
		return lat;
	}
	public void setLat(String lat) {
		this.lat = lat;
	}
	public String getLon() {
		return lon;
	}
	public void setLon(String lon) {
		this.lon = lon;
	}
	public String getGlat() {
		return glat;
	}
	public void setGlat(String glat) {
		this.glat = glat;
	}
	public String getGlon() {
		return glon;
	}
	public void setGlon(String glon) {
		this.glon = glon;
	}
	
	
	

}
